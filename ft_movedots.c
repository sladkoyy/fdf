#include "fdf.h"

void		ft_movedots(t_params *fdf, t_axis *axis)
{
	(*axis).x0 += (*fdf).startx;
	(*axis).y0 += (*fdf).starty;
	(*axis).x1 += (*fdf).startx;
	(*axis).y1 += (*fdf).starty;
}
